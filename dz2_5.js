for (let i = 1; i < 101; i++) {
    if (i < 18) {
        console.log(i + " - ребенок" + "\n");
    }
    else if (i < 31) {
        console.log(i + " - молодой" + "\n");
    }
    else if (i < 56) {
        console.log(i + " - зрелый" + "\n");
    }
    else {
        console.log(i + " - старый" + "\n");
    }
}